package cn.touchmedia.sean.mongo;

public class SignResponse {

	int status;
	String info;
	int data;
	
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public int getData() {
		return data;
	}
	public void setData(int data) {
		this.data = data;
	}

	
}
